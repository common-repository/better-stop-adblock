<p>L'equazione a cui dovete pensare &eacute; una : senza visitatori nessun guadagno ma con tanti visitatori senza i banner &eacute; la stessa cosa.</p>
<p>Quindi spetta a noi il compito di educare i visitatori ad un comportamento pi&uacute; corretto, visto che usufruiscono di tanti servizi in maniera gratuita e si chiede loro solamente un rapido caricamento di un banner.</p>
