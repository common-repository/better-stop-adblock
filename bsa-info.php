<h3>FAQ</h3>
<h3>I link agli script e le immagini vengono caricate su ogni visualizzazione di pagina?</h3>
<p>
    No. Nessuno dei link &eacute; davvero caricato (nemmeno sulla prima visualizzazione della pagina). Con o senza adblocker o con Javascript abilitati o disabilitati. Abbiamo semplicemente l'accesso solo al file che &eacute; gi&aacute; nella cache del browser stesso.
    <h3>Che cosa succede se il visitatore ha i Javascript disabilitati?</h3>
    <p>
        I visitatori riceveranno un messaggio per abilitare i javascript per poter accedere alla pagina.
        <h3>Con lo script sono escluso dai motori di ricerca?</h3>
        <p>
            No. Un crawler dei motori di ricerca non esegue codice HTML, come lo rende il vostro browser, ma semplicemente legge solo il codice sorgente della pagina. Dopo che il codice sorgente viene filtrato a taluni elementi (testo, immagini, link, ecc.). Cio&eacute; n&eacute; javascript n&eacute; css, tutto il resto viene eseguito. Google pu&oacute; prendere gli screenshot dalle pagine web, che portano dalla sorgente. Tuttavia, Google non utilizza nessun adblocker, quindi nessun messaggio.
